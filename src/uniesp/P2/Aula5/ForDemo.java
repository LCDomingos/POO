package uniesp.P2.Aula5;

public class ForDemo {
    public static void main(String[] args){
        int sum = 0;

        for(int k=1; k<=20; k++){
            sum = sum +k;
            System.out.println("Sum: "+sum);
        }
        System.out.println("*****************");
        System.out.println("Sum: "+sum);
        

    }

}
